    public function parse()
    {
        try{
            $client = new Client();

            $pecom_cities = $this->cities();

            $data = $client->get('http://www.pecom.ru/bitrix/components/pecom/calc/ajax.php', [
                'query' => [
                    'places[0]' => [
                        $this->width,
                        $this->depth,
                        $this->height,
                        $this->width*$this->depth*$this->height,
                        $this->weight
                    ],
                    'take[town]' => isset($pecom_cities[$this->from_city_name]) ? $pecom_cities[$this->from_city_name] : '',
                    'deliver[town]' => isset($pecom_cities[$this->to_city_name]) ? $pecom_cities[$this->to_city_name] : '',
                ]
            ]);

            $data = json_decode($data->getBody(), 1);
            if ($data !== null && isset($data['auto'][2])) {
                $this->res_status = self::STATUS_SUCCESS;
                $this->cost = $data['auto'][2] + $data['take'][2] + $data['deliver'][2] + 50;
            }

            return $this->checkCost();
        } catch (ClientException $e){
            $this->last_error_code = 1;
            $this->last_error_msg = $e->getMessage();

            return false;
        } catch (Exception $e){
            $this->last_error_code = 1;
            $this->last_error_msg = $e->getMessage();

            return false;
        }
    }

